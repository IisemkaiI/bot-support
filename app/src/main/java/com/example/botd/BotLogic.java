    private static String buildSimpleResponse(String base, String closing) {
        return base + " " + closing;
    }

    private static boolean containsAny(String text, String... words) {
        for (String word : words) {
            if (text.contains(word.toLowerCase())) {
                return true;
            }
        }
        return false;
    }

    private static String pickRandom(String... options) {
        if (options == null || options.length == 0) return "";
        return options[random.nextInt(options.length)];
    }

    public static void clearContext(String userId) {
        userContexts.remove(userId);
    }

    public static int getContextSize() {
        return userContexts.size();
    }
